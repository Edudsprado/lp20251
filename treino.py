# Questão:
# 
# Você tem um arquivo chamado "dados.csv" que contém várias linhas,
# onde cada linha é uma lista de números separados por ponto e vírgula (;).
# 
# Exemplo do conteúdo do arquivo:
# 10;20;30;40;50
# 15;20;25;30;35
# 10;15;20;25;30
#
# Faça um programa que:
# 1) Leia o arquivo linha por linha.
# 2) Para cada linha:
#    - Converta os números para inteiros.
#    - Conte quantos números são maiores que 25.
#    - Armazene esses números maiores que 25 em uma lista geral.
# 3) Ao final da leitura de todas as linhas:
#    - Crie uma lista contendo apenas os números únicos (não repetidos) da lista geral.
#    - Ordene essa lista única em ordem crescente.
# 4) Imprima:
#    - Para cada linha: a quantidade de números maiores que 25.
#    - Ao final: a lista ordenada dos números únicos maiores que 25 encontrados em todo o arquivo.
#
# Dica:
# Use estruturas de repetição, listas, contadores, set para eliminar repetidos,
# e funções como sorted() para ordenar.
